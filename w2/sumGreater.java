public class sumGreater {
  public static void main(String[] args) {
    int number1 = 15;
    int number2 = 20;

    int sum = number1 + number2;

    if (sum >= 20){
      System.out.println("The sum is greater then 20");
    }else{
      System.out.println("The sum is smaller than 20");
    }
  }
}
